function [KernelVz]=Tesseroid_IntegralkernelVz(R2,R1,FaiI,LamdaI,r,fai,lamda)

h2=r-R2;
hRatio2=h2/r;
h1=r-R1;
hRatio1=h1/r;

R2=1-hRatio2;
R1=1-hRatio1;
Phi=atan2(sqrt((cosd(FaiI).*sind(LamdaI-lamda)).^2+(cosd(fai).*sind(FaiI)-sind(fai).*cosd(FaiI).*cosd(LamdaI-lamda)).^2),...
    sind(fai).*sind(FaiI)+cosd(fai).*cosd(FaiI).*cosd(LamdaI-lamda));

l2pow1=(2.*(2.*sin(Phi/2).^2).*(1-hRatio2)+hRatio2.^2).^(0.5);
l1pow1=(2.*(2.*sin(Phi/2).^2).*(1-hRatio1)+hRatio1.^2).^(0.5);

KernelVz=cosd(FaiI).*(...
    3.*cos(Phi).*((l1pow1-l2pow1)./(l2pow1.*l1pow1))+...
    (1-6.*cos(Phi).^2).*((R2.*l1pow1-R1.*l2pow1)./(l2pow1.*l1pow1))+...
    cos(Phi).*((R2.^2.*l1pow1-R1.^2.*l2pow1)./(l2pow1.*l1pow1))+...
    (1-3.*cos(Phi).^2).*log((cos(Phi)-R2+l2pow1)./(cos(Phi)-R1+l1pow1)));

Index1=Phi<(pi/2-acos(1e-5));
Index2=Phi>(pi/2+acos(1e-5));

if R2<1

    KernelVz(Index1)=-cosd(FaiI(Index1))*(...
        (R2-R1)./...
        ((1-R2).*(1-R1))+...
        (R2-R1)+...
        2.*log((1-R2)./(1-R1)));

else
    if R1<1

        KernelVz(Index1)=cosd(FaiI(Index1))*(...
            (2-R2-R1)./...
            ((1-R2).*(1-R1))+...
            (R2+R1)+...
            4.*log(r)+...
            2.*log((1-R1).*(R2-1)));

    else

        KernelVz(Index1)=cosd(FaiI(Index1))*(...
            (R2-R1)./...
            ((1-R2).*(1-R1))+...
            (R2-R1)+...
            2.*log((1-R2)./(1-R1)));

    end
end

KernelVz(Index2)=cosd(FaiI(Index2))*(...
    (R1-R2)./...
    ((1+R2).*(1+R1))-...
    (R2-R1)+...
    2.*log((1+R2)./(1+R1)));

end